<template>
  <div class="descricao">
    <PageTitle icon="fa fa-asterisk" main="Descrição Padrão" sub="Cadastro de Descrição Padrão" />
    <div class="descricao-tab">
      <b-card no-body>
        <b-tabs v-model="tabIndex" card>
          <b-tab title="Lista" active>
            <div class="list">
              <div class="description-filter">
                <i class="fa fa-search fa-lg"></i>
                <input type="text" placeholder="Digite para filtrar..." v-model="descriptionFilter" 
                  class="filter-field">
              </div>
              <b-form>
                <input id="description-id" type="hidden" v-model="description.id" />
              </b-form>
              <b-table hover striped :items="descriptions" :fields="fields" :filter="descriptionFilter">
                <template slot="actions" slot-scope="data">
                  <b-button variant="warning" v-if="user.admin" @click="loadDescription(data.item), tabIndex++" class="mr-2">
                    <i class="fa fa-pencil" />
                  </b-button>
                  <b-button variant="danger" v-if="user.email === 'leandro@evspatrimonio.com.br' || user.email === 'everaldo@evspatrimonio.com.br' && user.admin" @click="loadDescription(data.item, 'remove'), tabIndex++">
                    <i class="fa fa-trash" />
                  </b-button>
                </template>
              </b-table>
            </div> 
          </b-tab>                 
          <b-tab title="Incluir" v-if="user.admin">
            <div class="include">
              <b-form>
                <input id="description-id" type="hidden" v-model="description.id" />
                <b-row>
                  <b-col md="6" sm="12">
                    <b-form-group label="Código: *" label-for="description-codigo">
                      <b-form-input id="description-codigo" type="text"
                        v-model="description.codigo" required
                        :readonly="mode === 'remove'"
                        placeholder="Informe o Código da Descrição Padrão..."/>
                    </b-form-group>
                  </b-col>
                  <b-col md="6" sm="12">
                    <b-form-group label="Nome: *" label-for="description-name">
                      <b-form-input id="description-name" type="text"
                        v-model="description.name" required
                        :readonly="mode === 'remove'"
                        placeholder="Informe o Nome da Descrição Padrão..."/>
                    </b-form-group>
                  </b-col>
                </b-row>
                <b-button variant="primary" v-if="mode === 'save'" @click="save">Salvar</b-button>
                <b-button variant="danger" v-if="mode === 'remove'" @click="remove">Excluir</b-button>
                <b-button class="ml-2" @click="reset">Cancelar</b-button>
              </b-form>
              <hr />
            </div>
          </b-tab>
        </b-tabs>
      </b-card>
    </div>
  </div>
</template>

<script>
import PageTitle from '../template/PageTitle'
import { baseApiUrl, showError } from '@/global'
import axios from 'axios'
import { mapState } from 'vuex'

export default {
    name: 'Descricao',
    components: { PageTitle },
    computed: mapState(['user']),
    data: function(){
      return{
        mode: 'save',
        description: {},
        descriptions: [],
        fields: [
          { key: 'codigo', label: 'Código', sortable: true },
          { key: 'name', label: 'Nome', sortable: true },
          { key: 'actions', label: 'Ações'}
        ],
        tabIndex: 1,
        descriptionFilter: '',
        filter: { emptyText: 'Descrição não encontrada.' }
      };
    },
    methods: {
        loadDescriptions(){
            const url = `${baseApiUrl}/descriptions`;
            axios.get(url).then(res =>{
                this.descriptions = res.data;
            })
        },
        reset() {
            this.mode = "save";
            this.description = {};
            this.loadDescriptions();
        },
        save() {
        const method = this.description.id ? "put" : "post";
        const id = this.description.id ? `/${this.description.id}` : "";
        axios[method](`${baseApiUrl}/descriptions${id}`, this.description)
            .then(() => {
                this.$toasted.global.defaultSuccess();
                this.reset();
            })
            .catch(showError);
        },
        remove() {
        const id = this.description.id;
        axios.delete(`${baseApiUrl}/descriptions/${id}`)
            .then(() => {
                this.$toasted.global.defaultSuccess();
                this.reset();
            })
            .catch(showError);
        },
        loadDescription(description, mode = "save") {
            this.mode = mode;
            this.description = { ...description };
        }
    },
    mounted(){
        this.loadDescriptions()
    } 
};
</script>
  
